function page2 () {
    var displayBox = document.querySelector(".page2 .displayBox");
    displayBox.addEventListener("mouseenter",function() {
        gsap.to(".page2 .displayBox img",{
            scale:1.05,
            duration:0.3,
            ease:"none",
            opacity:0.98,
        })
        gsap.to(".page2 .displayBox .capcation",{
            opacity:1,
            y:-25,
            duration:0.3,
            ease:"none",
        })
    })
    displayBox.addEventListener("mouseleave",function() {
        gsap.to(".page2 .displayBox img",{
            scale:1,
            duration:0.3,
            ease:"none",
            opacity:0.98,
        })
        gsap.to(".page2 .displayBox .capcation",{
            opacity:0,
            y:50,
        })
    })
}
page2();
